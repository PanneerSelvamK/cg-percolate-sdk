    /**
     * Build new X509TrustManager with default JVM certificates enhanced by custom certificates.
     *
     * @return {@link X509TrustManager} enhanced by custom certificates
     * @throws GeneralSecurityException {@link GeneralSecurityException}
     */
    public static X509TrustManager build() throws GeneralSecurityException {
        TrustManagerFactory factory = TrustManagerFactory.getInstance(TrustManagerFactory.getDefaultAlgorithm());
        factory.init((KeyStore) null);
        List<X509TrustManager> listOfTrustManagers = new ArrayList<>();
        for (TrustManager tm : factory.getTrustManagers()) {
            if (tm instanceof X509TrustManager) {
                listOfTrustManagers.add((X509TrustManager) tm);
            }
        }
        listOfTrustManagers.add(trustManagerForCertificates(trustedCertificatesInputStream()));
        return new CompositeX509TrustManager(listOfTrustManagers);
    }

